<?php

/**
 * Patches the Orchestra Testbench core Laravel app so that Laravel Boost
 * (laravel/roster) can detect the project's packages.
 *
 * Testbench's base Laravel app lives in vendor/orchestra/testbench-core/laravel/
 * and has a minimal composer.json with no dependencies, which means Boost sees
 * an empty package list. This script:
 *
 * 1. Injects the real dependencies into that composer.json.
 * 2. Creates a minimal composer.lock from the project's own composer.lock so
 *    laravel/roster (used by Boost) can detect installed packages.
 * 3. Creates a proxy vendor/autoload.php so tools like Intelephense and
 *    LaravelExtraIntellisense can invoke artisan directly without needing the
 *    TESTBENCH_WORKING_PATH environment variable.
 */
$testbenchBase = __DIR__ . '/../vendor/orchestra/testbench-core/laravel';

// --- 1. Patch composer.json ---
$composerJsonPath = $testbenchBase . '/composer.json';
$composerJsonRaw = file_get_contents($composerJsonPath);

if ($composerJsonRaw === false) {
    fwrite(STDERR, "Failed to read {$composerJsonPath}." . PHP_EOL);

    exit(1);
}

$composerData = json_decode($composerJsonRaw, true);

if (! is_array($composerData)) {
    fwrite(STDERR, "Invalid JSON in {$composerJsonPath}." . PHP_EOL);

    exit(1);
}

$composerData['require'] = [
    'filament/filament' => '^5.0',
    'filament/blueprint' => '^2.1',
    'livewire/livewire' => '^3.0',
    'spatie/laravel-package-tools' => '^1.15',
];
file_put_contents(
    $composerJsonPath,
    json_encode($composerData, JSON_PRETTY_PRINT | JSON_UNESCAPED_SLASHES) . PHP_EOL
);

// --- 2. Create minimal composer.lock from project lock ---
$projectLockPath = __DIR__ . '/../composer.lock';
$projectLockRaw = file_get_contents($projectLockPath);

if ($projectLockRaw === false) {
    fwrite(STDERR, "Failed to read {$projectLockPath}." . PHP_EOL);

    exit(1);
}

$lock = json_decode($projectLockRaw, true);

if (! is_array($lock)) {
    fwrite(STDERR, "Invalid JSON in {$projectLockPath}." . PHP_EOL);

    exit(1);
}

$keep = [
    'filament/filament',
    'filament/blueprint',
    'laravel/framework',
    'livewire/livewire',
    'larastan/larastan',
    'laravel/pint',
    'pestphp/pest',
    'rector/rector',
];

$allPackages = array_merge($lock['packages'] ?? [], $lock['packages-dev'] ?? []);
$filteredPackages = array_values(array_filter(
    $allPackages,
    fn (array $package): bool => in_array($package['name'], $keep, true)
));

$miniLock = [
    '_readme' => ['Generated by bin/patch-testbench-boost.php for Laravel Boost package detection.'],
    'content-hash' => 'boost-generated',
    'packages' => $filteredPackages,
    'packages-dev' => [],
    'aliases' => [],
    'minimum-stability' => 'dev',
    'stability-flags' => [],
    'prefer-stable' => false,
    'prefer-lowest' => false,
    'platform' => [],
    'platform-dev' => [],
];

file_put_contents(
    $testbenchBase . '/composer.lock',
    json_encode($miniLock, JSON_PRETTY_PRINT) . PHP_EOL
);

// --- 3. Create proxy vendor/ directory with autoload.php + package symlinks ---
// boost:install uses base_path('vendor/{package}') to locate packages, where
// base_path() resolves to this testbench laravel directory. Without symlinks,
// Boost cannot find filament/filament or filament/blueprint and skips their
// guidelines. We also need a proxy autoload.php so IDE tools (Intelephense,
// LaravelExtraIntellisense) can invoke artisan without TESTBENCH_WORKING_PATH.
$vendorDir = $testbenchBase . '/vendor';
if (! is_dir($vendorDir)) {
    mkdir($vendorDir, 0755, true);
}

$projectVendorDir = realpath(__DIR__ . '/../vendor');
$resolvedVendorDir = realpath($vendorDir);
$isSharedVendorDir = ($projectVendorDir !== false) && ($resolvedVendorDir === $projectVendorDir);

// Only create the proxy autoloader when Testbench has its own vendor directory.
// Some setups symlink testbench-core/laravel/vendor to the project's /vendor.
// In that case, writing autoload.php here would overwrite the root Composer autoload.
if (! $isSharedVendorDir) {
    file_put_contents(
        $vendorDir . '/autoload.php',
        "<?php\n\n// Proxy autoloader — see bin/patch-testbench-boost.php\nrequire __DIR__ . '/../../../../autoload.php';\n"
    );
}

// Symlinks for packages that have boost guidelines/skills so boost:install finds them
$realVendor = $projectVendorDir;
$packagesToLink = array_unique(array_keys(array_merge(
    $composerData['require'],
    $composerData['require-dev'] ?? []
)));

foreach ($packagesToLink as $package) {
    [$vendor, $name] = explode('/', $package, 2);
    $realPackagePath = $realVendor . '/' . $vendor . '/' . $name;

    if (! is_dir($realPackagePath)) {
        continue;
    }

    $linkVendorDir = $vendorDir . '/' . $vendor;
    if (! is_dir($linkVendorDir)) {
        mkdir($linkVendorDir, 0755, true);
    }

    $linkPath = $linkVendorDir . '/' . $name;

    if (is_link($linkPath)) {
        $currentTarget = realpath($linkPath);

        if ($currentTarget !== realpath($realPackagePath)) {
            unlink($linkPath);
            symlink($realPackagePath, $linkPath);
        }

        continue;
    }

    if (! file_exists($linkPath)) {
        symlink($realPackagePath, $linkPath);
    }
}

// Laravel package discovery reads metadata from vendor/composer under base_path().
// Keep that metadata reachable in the testbench skeleton vendor to avoid missing
// providers (e.g. Filament panel routes such as /admin).
$realComposerMetadataDir = $realVendor . '/composer';
$proxyComposerMetadataDir = $vendorDir . '/composer';

if (is_dir($realComposerMetadataDir) && ! file_exists($proxyComposerMetadataDir)) {
    symlink($realComposerMetadataDir, $proxyComposerMetadataDir);
}

// --- 4. Sync testbench.yaml to bootstrap/cache so artisan (via symlink) picks up
//        providers (e.g. IdeHelperServiceProvider) without TESTBENCH_WORKING_PATH. ---
$cacheDir = $testbenchBase . '/bootstrap/cache';
if (! is_dir($cacheDir)) {
    mkdir($cacheDir, 0755, true);
}
copy(__DIR__ . '/../testbench.yaml', $cacheDir . '/testbench.yaml');

echo 'Testbench Boost patch applied.' . PHP_EOL;
